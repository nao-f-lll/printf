#include <unistd.h>

int	ft_putstr(char *s)
{
	int	c;

	if (s != NULL)
	{
		c = 0;
		while (s && *s)
		{
			write(1, s++, 1);
			c++;
		}
		return (c);
	}
	else
		ft_putstr("(null)");
	return (c);
}
